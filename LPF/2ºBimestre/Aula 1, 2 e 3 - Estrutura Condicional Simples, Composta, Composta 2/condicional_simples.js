let idade = 25;

if (idade >= 18) {
  console.log('Você é maior de idade.');
} else {
  console.log('Você é menor de idade.');
}



/*Explicação: Neste exemplo, usamos uma estrutura condicional 
simples (if...else) para verificar se a variável idade é maior
 ou igual a 18. Se essa condição for verdadeira, o código dentro
  do bloco if é executado (imprime "Você é maior de idade.").
   Caso contrário, o código dentro do bloco else é executado
    (imprime "Você é menor de idade.").*/